def _parse_retry_delay(error: Exception) -> float:
    """Extract retry_delay seconds from a 429 error message. Default 5s."""
    err_str = str(error)
    # Look for patterns like "retry_delay { seconds: 30 }" or "retryDelay":"30s"
    m = _re.search(r'retry.?delay.*?(\d+)', err_str, _re.IGNORECASE)
    if m:
        return float(m.group(1))
    return 5.0


