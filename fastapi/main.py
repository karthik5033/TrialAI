@app.post("/mitigate-and-retrain")
async def mitigate_and_retrain(request: RetrainRequest):
    """Use LLM to modify training script, retrain model, re-evaluate."""
    session = load_session(request.session_id)
    if not session:
        return JSONResponse(
            {"error": f"Session '{request.session_id}' not found. The session may have expired or the analysis was not completed. Please re-run the analysis from /full-analysis."},
            status_code=404,
        )

    # Verify critical files still exist on disk
    dataset_path = Path(session["dataset_path"])
    model_path = Path(session["model_path"])
    if not dataset_path.exists():
        return JSONResponse(
            {"error": f"Dataset file missing at '{dataset_path}'. The workspace may have been cleaned. Please re-upload and re-analyze."},
            status_code=404,
        )
    if not model_path.exists():
        return JSONResponse(
            {"error": f"Model file missing at '{model_path}'. The workspace may have been cleaned. Please re-upload and re-analyze."},
            status_code=404,
        )

    if not get_gemini_key():
        return JSONResponse(
            {"error": "GEMINI_API_KEY is not configured on the server. Automated retraining requires a Gemini API key in the environment."},
            status_code=500,
        )

    workspace = Path(session["workspace"])
    output_model_path = str(workspace / "mitigated_model.pkl").replace("\\", "/")

    # ── Determine script to use ──
    script_content = session.get("script_content")
    if not script_content:
        # Check if a training script file exists on disk (may have been uploaded separately)
        workspace = Path(session["workspace"])
        disk_script = workspace / "training_script.py"
        if disk_script.exists():
            script_content = disk_script.read_text()
            print(f"[SESSION {request.session_id}] Loaded training script from disk")
        else:
            # Auto-generate a training script using Gemini
            print(f"[SESSION {request.session_id}] No script uploaded, auto-generating for {session['model_type']}")
            try:
                auto_prompt = f"""Generate a complete, self-contained Python training script for the following:

- Model type: {session['model_type']}
- Dataset path: "{str(session['dataset_path']).replace('\\\\', '/')}"
- Target column: "{session['target_column']}"
- Sensitive attributes: {session['sensitive_attributes']}
- Save trained model to: "{output_model_path}" using joblib.dump()

The script must:
1. Load the CSV, handle missing values, encode categorical features
2. Split into train/test, train the model
3. Apply fairness constraints using sklearn sample_weight or post-processing
4. Save the model with joblib.dump()
5. Print a JSON line: {{"accuracy": 0.XX, "status": "success"}}
6. Be fully runnable with no external dependencies beyond sklearn, pandas, numpy, joblib

Return ONLY the Python code, no markdown fences or explanations."""
                script_content = await rotator.generate_content(auto_prompt)
                script_content = script_content.strip()
                if script_content.startswith("```"):
                    script_content = script_content.split("\n", 1)[1].rsplit("```", 1)[0].strip()
                print(f"[SESSION {request.session_id}] Auto-generated training script ({len(script_content)} chars)")
            except Exception as gen_err:
                print(f"[SESSION {request.session_id}] Gemini auto-gen failed: {gen_err}, using fallback")
                script_content = generate_fallback_training_script(
                    model_type=session["model_type"],
                    dataset_path=str(session["dataset_path"]).replace("\\", "/"),
                    target_column=session["target_column"],
                    sensitive_attrs=session["sensitive_attributes"],
                    output_model_path=output_model_path,
                )

    try:
        # ── Generate mitigated code — SINGLE LLM call, fast fallback ──
        import sys
        venv_python = sys.executable
        python_path = os.environ.get("PYTHON_PATH", venv_python)
        modified_script_path = workspace / "modified_training_script.py"

        modified_code = None
        retrain_success = False
        retrain_stdout = ""
        retrain_stderr = ""
        max_attempts = 2

        dataset_path_str = str(session["dataset_path"]).replace("\\", "/")
        target_col = session["target_column"]
        sens_attrs = session["sensitive_attributes"]

        bias_context = json.dumps({
            "fairness_metrics": session["fairness_metrics"],
            "proxy_features": session.get("proxies", []),
        }, indent=2)

        def _clean_code_block(text: str) -> str:
            text = text.strip()
            if text.startswith("```"):
                text = text.split("\n", 1)[1].rsplit("```", 1)[0].strip()
            return text

        for attempt in range(max_attempts):
            if attempt == 0:
                # ── ONE single LLM call to rewrite the script ──
                prompt = f"""Rewrite this Python training script to add bias mitigation.

ORIGINAL SCRIPT:
```python
{script_content}
```

BIAS ANALYSIS:
{bias_context}

REQUIREMENTS:
1. Read dataset from: "{dataset_path_str}"
2. Target column: "{target_col}"
3. Sensitive attributes: {sens_attrs}
4. Add sample reweighting with compute_sample_weight("balanced", y)
5. Add class_weight="balanced" to the model if supported
6. Remove proxy features correlated with sensitive attributes if any were detected
7. Save model to: "{output_model_path}" using joblib.dump()
8. Print JSON to stdout: {{"accuracy": 0.XX, "status": "success"}}
9. Fully self-contained, runnable with `python script.py`
10. Handle missing values, encode categoricals with LabelEncoder

Return ONLY the Python script, no markdown fences or explanations."""

                try:
                    print(f"[SESSION {request.session_id}] Single LLM call for mitigated script...")
                    modified_code = await rotator.generate_content(prompt, fan_out=2)
                    modified_code = _clean_code_block(modified_code)
                    print(f"[SESSION {request.session_id}] Got mitigated script ({len(modified_code)} chars)")
                except Exception as llm_err:
                    # Instant rule-based fallback — zero API calls
                    print(f"[SESSION {request.session_id}] LLM failed ({llm_err}), using rule-based fallback")
                    modified_code = generate_rule_based_fallback(
                        script_content=script_content,
                        dataset_path=dataset_path_str,
                        target_column=target_col,
                        sensitive_attrs=sens_attrs,
                        output_model_path=output_model_path,
                        proxy_features=session.get("proxies", []),
                    )
            else:
                # Retry: feed the error back
                print(f"[SESSION {request.session_id}] Retry — feeding error back to LLM")
                try:
                    fix_prompt = f"""Fix this Python script that failed with an error.

FAILED SCRIPT:
```python
{modified_code}
```

ERROR:
```
{retrain_stderr[-800:]}
```

Fix the error. Dataset: "{dataset_path_str}", target: "{target_col}", save to: "{output_model_path}".
Print JSON: {{"accuracy": 0.XX, "status": "success"}}. Return ONLY the fixed Python script."""

                    modified_code = await rotator.generate_content(fix_prompt, fan_out=2)
                    modified_code = _clean_code_block(modified_code)
                except Exception:
                    # If LLM fails on retry too, use rule-based
                    modified_code = generate_rule_based_fallback(
                        script_content=script_content,
                        dataset_path=dataset_path_str,
                        target_column=target_col,
                        sensitive_attrs=sens_attrs,
                        output_model_path=output_model_path,
                        proxy_features=session.get("proxies", []),
                    )

            # Write and execute
            modified_script_path.write_text(modified_code)
            print(f"[SESSION {request.session_id}] Attempt {attempt+1}/{max_attempts} - executing with: {python_path}")

            result = subprocess.run(
                [python_path, str(modified_script_path)],
                cwd=str(workspace),
                capture_output=True,
                text=True,
                timeout=120,
            )

            retrain_stdout = result.stdout
            retrain_stderr = result.stderr
            retrain_success = result.returncode == 0 and Path(output_model_path).exists()

            print(f"[SESSION {request.session_id}] Attempt {attempt+1} exit={result.returncode}, model_exists={Path(output_model_path).exists()}")
            if retrain_stderr:
                print(f"[SESSION {request.session_id}] STDERR (last 300): {retrain_stderr[-300:]}")

            if retrain_success:
                break

        if not retrain_success:
            return {
                "session_id": request.session_id,
                "retrain_success": False,
                "error": f"Retrain failed after {max_attempts} attempts (exit code {result.returncode})",
                "stdout": retrain_stdout[-2000:] if retrain_stdout else "",
                "stderr": retrain_stderr[-2000:] if retrain_stderr else "",
                "original_script": script_content,
                "modified_script": modified_code,
                "before": {k: float(session["fairness_metrics"][k]) for k in ("demographic_parity", "equal_opportunity", "disparate_impact")},
            }

        # ── Re-evaluate the retrained model ──
        new_model = load_model_file(output_model_path)
        df = pd.read_csv(session["dataset_path"])
        X, y, feature_cols, raw_sensitive, _ = prepare_dataset(
            df, session["target_column"], session["sensitive_attributes"]
        )

        sensitive_col_key = next(
            (k for k in raw_sensitive if k.lower() == session["sensitive_attributes"][0].lower()), None
        )
        sensitive_values = raw_sensitive[sensitive_col_key] if sensitive_col_key else np.zeros(len(y))

        # Smart feature matching (same logic as /full-analysis)
        try:
            y_pred_new = new_model.predict(X.values)
        except Exception:
            matched = False
            if hasattr(new_model, "feature_names_in_"):
                model_features = list(new_model.feature_names_in_)
                available = [f for f in model_features if f in X.columns]
                if len(available) == len(model_features):
                    X = X[available]
                    y_pred_new = new_model.predict(X.values)
                    matched = True

            if not matched and hasattr(new_model, "n_features_in_"):
                n_expected = new_model.n_features_in_
                X_numeric = X.select_dtypes(include=[np.number])
                if len(X_numeric.columns) >= n_expected:
                    X = X_numeric.iloc[:, :n_expected]
                    y_pred_new = new_model.predict(X.values)
                    matched = True

            if not matched:
                # Last resort: try numeric-only
                X_numeric = X.select_dtypes(include=[np.number])
                y_pred_new = new_model.predict(X_numeric.values)

        new_accuracy = round(accuracy_score(y, y_pred_new), 4)
        after_metrics = compute_fairlearn_metrics(y, y_pred_new, sensitive_values)

        # ── Compute improvement ──
        before = {k: float(session["fairness_metrics"][k]) for k in ("demographic_parity", "equal_opportunity", "disparate_impact")}
        after = {k: float(after_metrics[k]) for k in ("demographic_parity", "equal_opportunity", "disparate_impact")}
        improvement = {}
        for key in before:
            bv = before[key]
            av = after[key]
            improvement[key] = round(float(((av - bv) / bv) * 100), 1) if bv > 0 else 0.0

        retrial_passed = all(after.get(k, 0) >= 0.80 for k in after)

        # Update session (memory + disk)
        sessions[request.session_id]["mitigated_model_path"] = output_model_path
        sessions[request.session_id]["modified_script"] = modified_code
        sessions[request.session_id]["after_metrics"] = after_metrics
        save_session(request.session_id, sessions[request.session_id])

        return {
            "session_id": request.session_id,
            "retrain_success": True,
            "original_script": script_content,
            "modified_script": modified_code,
            "before": before,
            "after": after,
            "improvement": improvement,
            "original_accuracy": float(session["accuracy"]),
            "new_accuracy": float(new_accuracy),
            "retrial_passed": retrial_passed,
            "model_type": type(new_model).__name__,
            "summary": f"Retrained {type(new_model).__name__}. Accuracy: {session['accuracy']} -> {new_accuracy}.",
        }

    except subprocess.TimeoutExpired:
        return JSONResponse(
            {"error": "Retrain script timed out after 120 seconds. The dataset may be too large or the generated script may have an infinite loop. Try uploading a smaller dataset or provide your own training script."},
            status_code=500,
        )
    except Exception as e:
        traceback.print_exc()
        return JSONResponse(
            {"error": f"Mitigation failed: {str(e)}. Check the server logs for a full traceback."},
            status_code=500,
        )


