class GeminiRotator:
    """Round-robin key manager with retry_delay-aware 429 handling."""

    MODEL_NAME = "gemini-2.0-flash"

    def __init__(self):
        self.keys = []
        if os.environ.get("GEMINI_API_KEY"):
            self.keys.append(os.environ.get("GEMINI_API_KEY"))
        for i in range(1, 13):
            k = os.environ.get(f"GEMINI_API_KEY_{i}") or os.environ.get(f"GOOGLE_API_KEY_{i}")
            if k and k not in self.keys:
                self.keys.append(k)
        self.idx = 0
        print(f"[GeminiRotator] Loaded {len(self.keys)} API keys")

    def get_next_key(self):
        if not self.keys:
            return ""
        k = self.keys[self.idx]
        self.idx = (self.idx + 1) % len(self.keys)
        return k

    def _get_n_keys(self, n: int) -> list:
        """Get n distinct keys for parallel use."""
        n = min(n, len(self.keys))
        keys = []
        for _ in range(n):
            keys.append(self.get_next_key())
        return keys

    async def _call_with_key(self, prompt: str, api_key: str) -> str:
        """Single LLM call using direct HTTP REST API (no deprecated SDK)."""
        url = f"https://generativelanguage.googleapis.com/v1beta/models/{self.MODEL_NAME}:generateContent"
        body = {
            "contents": [{"parts": [{"text": prompt}]}],
            "generationConfig": {"temperature": 0.3, "maxOutputTokens": 8192},
        }
        async with httpx.AsyncClient(timeout=90) as client:
            resp = await client.post(url, json=body, params={"key": api_key})
            resp.raise_for_status()
            data = resp.json()
            return data["candidates"][0]["content"]["parts"][0]["text"]

    def _is_rate_limit(self, e: Exception) -> bool:
        err_str = str(e).lower()
        return "429" in err_str or "resourceexhausted" in err_str or "quota" in err_str

    def _is_model_not_found(self, e: Exception) -> bool:
        err_str = str(e).lower()
        return "404" in err_str or "not found" in err_str or "not supported" in err_str

    # ── Sequential with retry_delay: try each key, wait the server-specified delay on 429 ──
    async def generate_content(self, prompt: str, fan_out: int = 4) -> str:
        """Try keys with fan-out. On 429, wait the retry_delay before next key."""
        if not self.keys:
            raise ValueError("No Gemini keys found in environment.")

        fan_out = min(fan_out, len(self.keys))
        keys_to_use = self._get_n_keys(fan_out)

        # Create tasks for each key
        async def _try_key(key: str, idx: int) -> str:
            try:
                result = await self._call_with_key(prompt, key)
                print(f"[GeminiRotator] Key slot {idx} responded ✓")
                return result
            except Exception as e:
                if self._is_rate_limit(e):
                    delay = _parse_retry_delay(e)
                    print(f"[GeminiRotator] Key slot {idx} hit 429, retry_delay={delay}s")
                    await asyncio.sleep(delay)
                raise

        # Race all keys — return the first successful result
        tasks = [asyncio.create_task(_try_key(k, i)) for i, k in enumerate(keys_to_use)]
        pending = set(tasks)
        last_err = None

        while pending:
            finished, pending = await asyncio.wait(pending, return_when=asyncio.FIRST_COMPLETED)
            for task in finished:
                try:
                    result = task.result()
                    for p in pending:
                        p.cancel()
                    return result
                except Exception as e:
                    last_err = e
                    continue

        # All fan-out failed — sequential retry on remaining keys with delay
        print(f"[GeminiRotator] All {fan_out} parallel calls failed. Sequential retry with delay...")
        for key in self.keys:
            if key not in keys_to_use:
                try:
                    return await self._call_with_key(prompt, key)
                except Exception as e:
                    if self._is_rate_limit(e):
                        delay = _parse_retry_delay(e)
                        print(f"[GeminiRotator] Sequential key hit 429, waiting {delay}s...")
                        await asyncio.sleep(delay)
                    last_err = e
                    continue

        raise Exception(f"All keys exhausted. Last error: {last_err}")

    # ── Parallel multi-task: run DIFFERENT prompts on DIFFERENT keys simultaneously ──
    async def generate_parallel_tasks(self, prompts: list[str]) -> list[str]:
        """Run N different prompts across N different keys concurrently.
        Returns a list of results in the same order as prompts."""
        if not self.keys:
            raise ValueError("No Gemini keys found in environment.")

        n = len(prompts)
        keys = self._get_n_keys(n)

        async def _run_task(prompt: str, key: str, idx: int) -> tuple[int, str]:
            try:
                result = await self._call_with_key(prompt, key)
                print(f"[GeminiRotator] Task {idx} completed ✓")
                return (idx, result)
            except Exception as e:
                if self._is_rate_limit(e):
                    delay = _parse_retry_delay(e)
                    print(f"[GeminiRotator] Task {idx} hit 429, waiting {delay}s before fallback")
                    await asyncio.sleep(delay)
                # Retry with remaining keys
                for fallback_key in self.keys:
                    if fallback_key != key:
                        try:
                            result = await self._call_with_key(prompt, fallback_key)
                            print(f"[GeminiRotator] Task {idx} completed on fallback ✓")
                            return (idx, result)
                        except Exception as fe:
                            if self._is_rate_limit(fe):
                                delay = _parse_retry_delay(fe)
                                await asyncio.sleep(delay)
                            continue
                raise Exception(f"Task {idx} failed on all keys")

        tasks = [_run_task(p, k, i) for i, (p, k) in enumerate(zip(prompts, keys))]
        results = await asyncio.gather(*tasks, return_exceptions=True)

        output = [""] * n
        for r in results:
            if isinstance(r, Exception):
                raise r
            idx, text = r
            output[idx] = text
        return output

