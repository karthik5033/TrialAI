def save_session(session_id: str, session_data: dict) -> None:
    """Persist session data to disk as JSON for cross-restart durability."""
    workspace = Path(session_data.get("workspace", WORKSPACE_DIR / session_id))
    workspace.mkdir(parents=True, exist_ok=True)
    session_file = workspace / "session.json"
    # Write a JSON-safe copy (strip non-serializable fields)
    safe_data = {}
    for k, v in session_data.items():
        try:
            json.dumps(v)
            safe_data[k] = v
        except (TypeError, ValueError):
            safe_data[k] = str(v)
    session_file.write_text(json.dumps(safe_data, indent=2))


def load_session(session_id: str) -> dict | None:
    """Load session from memory, falling back to disk if not found."""
    if session_id in sessions:
        return sessions[session_id]
    # Try disk
    session_file = WORKSPACE_DIR / session_id / "session.json"
    if session_file.exists():
        try:
            data = json.loads(session_file.read_text())
            sessions[session_id] = data  # Re-hydrate into memory
            print(f"[SESSION {session_id}] Restored from disk")
            return data
        except Exception as e:
            print(f"[SESSION {session_id}] Failed to restore from disk: {e}")
    return None


